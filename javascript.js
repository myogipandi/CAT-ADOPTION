
function signup() {
  document.getElementById("up").style.display = "block";
  document.getElementById("log").style.display = "none";
}

function login() {
  document.getElementById("log").style.display = "block";
  document.getElementById("up").style.display = "none";
}

function clos() {
  document.getElementById("up").style.display = "none";
  document.getElementById("log").style.display = "none";
}